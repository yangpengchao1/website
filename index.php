    /**
     * 检查用户注册数据
     * @param $member_array_datas
     * @param $sign_up_password_repeat
     * @return array
     */
    function check_sign_up_member_datas($member_array_datas)
    {
        $result_flag = false;

        if (empty($member_array_datas["email"])) {
            return array($result_flag, "Email is required");
        } else {
            $email = test_input($member_array_datas["email"]);
            // check if e-mail address is well-formed
            if (!filter_var($email, FILTER_VALIDATE_EMAIL)) {
                return array($result_flag, "Invalid email format");
            }
        }

        if (empty($member_array_datas["name"])) {
            return array($result_flag, "Name is required");
        } else {
            $last_name = test_input($member_array_datas["last-name"]);
            // check if name only contains letters and whitespace
            if (!preg_match("/^[a-zA-Z ]*$/", $last_name)) {
                return array($result_flag, "Name field is only letters and white space allowed");
            }
        }

        if (empty($member_array_datas["phone"])) {
            return array($result_flag, "Phone number is required");
        } else {
            $mobile_number = test_input($member_array_datas["mobile-number"]);
            if (!preg_match("/^[0-9]{6,13}$/", $mobile_number)) {
                return array($result_flag, "Invalid mobile number format");
            }
        }

        $result_flag = true;
        return array($result_flag);
    }
